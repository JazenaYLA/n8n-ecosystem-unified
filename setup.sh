start_stack() {
  log "Starting Docker stack..."
  cd "$BASEDIR"
  docker compose pull --quiet
  docker compose up -d
  log "Stack started. Waiting for n8n to be ready..."
  local retries=30
  while ! curl -sf http://localhost:5678/healthz &>/dev/null; do
    sleep 3
    retries=$((retries-1))
    if [[ $retries -le 0 ]]; then
      warn "n8n did not respond after 90s. Check: docker compose logs n8n"
      break
    fi
  done
  log "n8n is up."
}

print_next_steps() {
  echo -e "\n${BOLD}${GREEN}=== Setup Complete ===${RESET}\n"
  echo -e "  n8n:           ${CYAN}https://${N8N_DOMAIN}${RESET}"
  echo -e "  Supabase Studio: ${CYAN}http://127.0.0.1:3001${RESET} (SSH tunnel recommended)"
  echo -e "  SearXNG:         ${CYAN}http://127.0.0.1:8888${RESET} (internal only)"
  echo -e ""
  echo -e "${BOLD}Next steps:${RESET}"
  echo -e "  1. Open n8n and complete initial account setup"
  echo -e "  2. Go to Settings → API and generate your N8N_API_KEY"
  echo -e "  3. Update .env with the API key, then: docker compose restart n8n"
  echo -e "  4. Import workflows from workflows/unified/ via n8n UI → Import"
  echo -e "  5. Set n8n Variables: TELEGRAM_CHAT_ID, EVOLUTION_INSTANCE_NAME, WHATSAPP_PHONE"
  echo -e "  6. Create credentials in n8n matching the names in .env.example"
  echo -e ""
  echo -e "${BOLD}Full docs:${RESET} docs/PROXMOX_SETUP.md"
}

main() {
  require_root
  check_proxmox_lxc
  collect_inputs
  generate_secrets
  generate_jwt_keys
  write_env
  write_searxng_settings
  write_kong_deployed
  install_docker
  install_caddy
  write_caddyfile
  start_stack
  print_next_steps
}

main "$@"
