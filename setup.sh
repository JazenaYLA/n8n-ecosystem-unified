  log ".env written."
}

write_searxng_settings() {
  # Replace {{SEARXNG_SECRET_KEY}} placeholder in searxng/settings.yml
  if [ -f "$BASEDIR/searxng/settings.yml" ]; then
    sed -i "s|{{SEARXNG_SECRET_KEY}}|${SEARXNG_SECRET_KEY}|g" "$BASEDIR/searxng/settings.yml"
    log "SearXNG settings.yml updated with generated secret key."
  fi
}

write_kong_deployed() {
  # Generate supabase/kong.deployed.yml with real keys
  if [ -f "$BASEDIR/supabase/kong.yml" ]; then
    sed "s|{{SUPABASE_ANON_KEY}}|${SUPABASE_ANON_KEY}|g;s|{{SUPABASE_SERVICE_KEY}}|${SUPABASE_SERVICE_KEY}|g" \
      "$BASEDIR/supabase/kong.yml" > "$BASEDIR/supabase/kong.deployed.yml"
    log "supabase/kong.deployed.yml generated."
  fi
}

write_caddyfile() {
  CADDYFILE="/etc/caddy/Caddyfile"
  log "Writing Caddyfile for ${N8N_DOMAIN}..."
  # Backup existing
  [ -f "$CADDYFILE" ] && cp "$CADDYFILE" "${CADDYFILE}.bak.$(date +%s)"
  cat >> "$CADDYFILE" <<EOF

# n8n-ecosystem-unified
${N8N_DOMAIN} {
    reverse_proxy localhost:5678
    encode gzip
    header {
        Strict-Transport-Security "max-age=31536000; includeSubDomains"
        X-Content-Type-Options nosniff
        X-Frame-Options DENY
    }
}
EOF
  systemctl reload caddy 2>/dev/null || systemctl restart caddy
  log "Caddy configured for https://${N8N_DOMAIN}"
}

prompt() {
  local var_name="$1"
  local prompt_text="$2"
  local default="${3:-}"
  local secret="${4:-false}"

  if [[ -n "$default" ]]; then
    echo -en "${CYAN}${prompt_text}${RESET} [${default}]: "
  else
    echo -en "${CYAN}${prompt_text}${RESET}: "
  fi

  if [[ "$secret" == "true" ]]; then
    read -rs value
    echo
  else
    read -r value
  fi

  if [[ -z "$value" && -n "$default" ]]; then
    value="$default"
  fi

  printf -v "$var_name" '%s' "$value"
}

