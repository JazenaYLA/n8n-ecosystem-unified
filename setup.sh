# DO NOT commit this file

# n8n
N8N_URL=https://${N8N_DOMAIN}
N8N_INTERNAL_URL=http://n8n:5678
N8N_API_KEY=
N8N_HOST=${N8N_DOMAIN}
N8N_PROTOCOL=https
N8N_WEBHOOK_URL=https://${N8N_DOMAIN}
