#!/usr/bin/env bash
# =============================================================
# n8n-ecosystem-unified — Setup Script
# Proxmox LXC / Debian 12 (bookworm) edition
# Based on JazenaYLA/n8n-claw setup.sh
#
# Differences from n8n-claw setup.sh:
#   - Caddy reverse proxy (not Nginx)
#   - Proxmox LXC detection and Docker nesting check
#   - Multi-channel env vars (WhatsApp/Evolution API)
#   - LLM provider selection: OpenRouter OR Anthropic direct
#   - CTI stack env vars (MISP, Wazuh, OpenCTI, TheHive, Cortex)
#   - SearXNG secret key generation
# =============================================================
set -euo pipefail

RED='\033[0;31m'; YELLOW='\033[1;33m'; GREEN='\033[0;32m'
CYAN='\033[0;36m'; BOLD='\033[1m'; RESET='\033[0m'

BASEDIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ENV_FILE="$BASEDIR/.env"

log()  { echo -e "${GREEN}[setup]${RESET} $*"; }
warn() { echo -e "${YELLOW}[warn]${RESET} $*"; }
err()  { echo -e "${RED}[error]${RESET} $*" >&2; }

require_root() {
  if [[ $EUID -ne 0 ]]; then
    err "Run as root: sudo bash setup.sh"
    exit 1
  fi
}

check_proxmox_lxc() {
  if grep -q 'container=lxc' /proc/1/environ 2>/dev/null || [ -f /run/container_type ]; then
    log "Proxmox LXC detected."
    if ! grep -q 'nesting=1' /proc/mounts 2>/dev/null; then
      warn "LXC nesting may not be enabled. If Docker fails, enable nesting in Proxmox: pct set <id> --features nesting=1"
    fi
  fi
}

install_docker() {
  if command -v docker &>/dev/null; then
    log "Docker already installed: $(docker --version)"
    return
  fi
  log "Installing Docker..."
  apt-get update -qq
  apt-get install -y -qq ca-certificates curl gnupg lsb-release
  install -m 0755 -d /etc/apt/keyrings
  curl -fsSL https://download.docker.com/linux/debian/gpg | gpg --dearmor -o /etc/apt/keyrings/docker.gpg
  chmod a+r /etc/apt/keyrings/docker.gpg
  echo "deb [arch=$(dpkg --print-architecture) signed-by=/etc/apt/keyrings/docker.gpg] \
    https://download.docker.com/linux/debian $(lsb_release -cs) stable" \
    > /etc/apt/sources.list.d/docker.list
  apt-get update -qq
  apt-get install -y -qq docker-ce docker-ce-cli containerd.io docker-compose-plugin
  systemctl enable docker
  systemctl start docker
  log "Docker installed."
}

install_caddy() {
  if command -v caddy &>/dev/null; then
    log "Caddy already installed."
    return
  fi
  log "Installing Caddy..."
  apt-get install -y -qq debian-keyring debian-archive-keyring apt-transport-https
  curl -1sLf 'https://dl.cloudsmith.io/public/caddy/stable/gpg.key' | \
    gpg --dearmor -o /usr/share/keyrings/caddy-stable-archive-keyring.gpg
  curl -1sLf 'https://dl.cloudsmith.io/public/caddy/stable/debian.deb.txt' | \
    tee /etc/apt/sources.list.d/caddy-stable.list
  apt-get update -qq
  apt-get install -y -qq caddy
  log "Caddy installed."
}

generate_secrets() {
  log "Generating secrets..."
  N8N_ENCRYPTION_KEY=$(openssl rand -hex 32)
  SUPABASE_JWT_SECRET=$(openssl rand -hex 32)
  SEARXNG_SECRET_KEY=$(openssl rand -hex 32)
  POSTGRES_PASSWORD=$(openssl rand -hex 16)
  log "Secrets generated."
}

generate_jwt_keys() {
  # Generate Supabase anon/service keys from JWT secret
  # These are standard Supabase JWT payloads
  # For production, use supabase CLI: supabase start
  # For homelab self-hosted, these static tokens are acceptable
  HEADER=$(echo -n '{"alg":"HS256","typ":"JWT"}' | base64 -w0 | sed 's/=//g;s/+/-/g;s|/|_|g')
  
  ANON_PAYLOAD=$(echo -n '{"role":"anon","iss":"supabase","iat":1700000000,"exp":2000000000}' | base64 -w0 | sed 's/=//g;s/+/-/g;s|/|_|g')
  SERVICE_PAYLOAD=$(echo -n '{"role":"service_role","iss":"supabase","iat":1700000000,"exp":2000000000}' | base64 -w0 | sed 's/=//g;s/+/-/g;s|/|_|g')

  sign() {
    local payload="$1"
    local data="$HEADER.$payload"
    echo -n "$data" | openssl dgst -sha256 -hmac "$SUPABASE_JWT_SECRET" -binary | base64 -w0 | sed 's/=//g;s/+/-/g;s|/|_|g'
  }

  SUPABASE_ANON_KEY="$HEADER.$ANON_PAYLOAD.$(sign "$ANON_PAYLOAD")"
  SUPABASE_SERVICE_KEY="$HEADER.$SERVICE_PAYLOAD.$(sign "$SERVICE_PAYLOAD")"
}

