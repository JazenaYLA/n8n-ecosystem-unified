# Supabase
SUPABASE_URL=http://kong:8000
SUPABASE_PUBLIC_URL=http://127.0.0.1:8000
SUPABASE_ANON_KEY=${SUPABASE_ANON_KEY}
SUPABASE_SERVICE_KEY=${SUPABASE_SERVICE_KEY}
SUPABASE_JWT_SECRET=${SUPABASE_JWT_SECRET}

# PostgreSQL
